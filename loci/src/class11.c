void
of_action_ofdpa_wire_object_id_get(of_object_t *obj, of_object_id_t *id)
{
    unsigned char *buf = OF_OBJECT_BUFFER_INDEX(obj, 0);
    switch (obj->version) {
    case OF_VERSION_1_3: {
        uint16_t value = U16_NTOH(*(uint16_t *)(buf + 8)); /* exp_type */
        switch (value) {
        case 0x1:
            *id = OF_ACTION_OFDPA_PUSH_L2_HEADER;
            break;
        case 0x2:
            *id = OF_ACTION_OFDPA_POP_L2_HEADER;
            break;
        case 0x3:
            *id = OF_ACTION_OFDPA_PUSH_CW;
            break;
        case 0x4:
            *id = OF_ACTION_OFDPA_POP_CW;
            break;
        default:
            *id = OF_ACTION_OFDPA;
            break;
        }
        break;
    }
    default:
        LOCI_ASSERT(0);
    }
}


