            break;
        case 0x3:
            *id = OF_INSTRUCTION_ID_WRITE_ACTIONS;
            break;
        case 0x4:
            *id = OF_INSTRUCTION_ID_APPLY_ACTIONS;
            break;
        case 0x5:
            *id = OF_INSTRUCTION_ID_CLEAR_ACTIONS;
            break;
