/**
 * Create a new of_ieee80211_vht_mcs_info object
 *
 * @param version The wire version to use for the object
 * @return Pointer to the newly create object or NULL on error
 *
 * Initializes the new object with it's default fixed length associating
 * a new underlying wire buffer.
 *
 * \ingroup of_ieee80211_vht_mcs_info
 */

of_object_t *
of_ieee80211_vht_mcs_info_new(of_version_t version)
{
    of_object_t *obj;
    int bytes;

    bytes = of_object_fixed_len[version][OF_IEEE80211_VHT_MCS_INFO];

    if ((obj = of_object_new(bytes)) == NULL) {
        return NULL;
    }

    of_ieee80211_vht_mcs_info_init(obj, version, bytes, 0);

    return obj;
}

/**
 * Initialize an object of type of_ieee80211_vht_mcs_info.
 *
 * @param obj Pointer to the object to initialize
 * @param version The wire version to use for the object
 * @param bytes How many bytes in the object
 * @param clean_wire Boolean: If true, clear the wire object control struct
 *
 * If bytes < 0, then the default fixed length is used for the object
 *
 * This is a "coerce" function that sets up the pointers for the
 * accessors properly.
 *
 * If anything other than 0 is passed in for the buffer size, the underlying
 * wire buffer will have 'grow' called.
 */

void
of_ieee80211_vht_mcs_info_init(of_object_t *obj,
    of_version_t version, int bytes, int clean_wire)
{
    LOCI_ASSERT(of_object_fixed_len[version][OF_IEEE80211_VHT_MCS_INFO] >= 0);
    if (clean_wire) {
        MEMSET(obj, 0, sizeof(*obj));
    }
    if (bytes < 0) {
        bytes = of_object_fixed_len[version][OF_IEEE80211_VHT_MCS_INFO];
    }
    obj->version = version;
    obj->length = bytes;
    obj->object_id = OF_IEEE80211_VHT_MCS_INFO;

    /* Grow the wire buffer */
    if (obj->wbuf != NULL) {
        int tot_bytes;

        tot_bytes = bytes + obj->obj_offset;
        of_wire_buffer_grow(obj->wbuf, tot_bytes);
    }
}

